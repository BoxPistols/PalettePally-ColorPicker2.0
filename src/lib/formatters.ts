// ── Import detection ──

export type ImportResult =
  | { format: 'json'; data: PaletteData }
  | { format: 'dtcg'; data: Partial<PaletteData> }
  | { format: 'tokensStudio'; data: Partial<PaletteData> }
  | { format: 'unknown'; error: string };

export function detectAndParse(text: string): ImportResult {
  let parsed: unknown;
  try {
    parsed = JSON.parse(text);
  } catch {
    return { format: 'unknown', error: 'Invalid JSON' };
  }

  if (typeof parsed !== 'object' || parsed === null) {
    return { format: 'unknown', error: 'Not a JSON object' };
  }

  const obj = parsed as Record<string, unknown>;

  // Native JSON: has colors/names/palette
  if (Array.isArray(obj.colors) && Array.isArray(obj.names) && Array.isArray(obj.palette)) {
    return { format: 'json', data: parsed as PaletteData };
  }

  // Tokens Studio: has global.color
  if (obj.global && typeof obj.global === 'object' && 'color' in (obj.global as object)) {
    return {
      format: 'tokensStudio',
      data: parseTokensStudio(obj.global as Record<string, unknown>),
    };
  }

  // DTCG: has action-colors or grey with $value
  if ('action-colors' in obj || 'grey' in obj || 'utility' in obj) {
    return { format: 'dtcg', data: { colors: [], names: [], palette: [], themeTokens: null, numColors: 0 } };
  }

  return { format: 'unknown', error: 'Unrecognized format' };
}

function parseTokensStudio(global: Record<string, unknown>): Partial<PaletteData> {
  const color = (global.color ?? {}) as Record<string, unknown>;
  const colors: string[] = [];
  const names: string[] = [];

  for (const [groupName, group] of Object.entries(color)) {
    if (typeof group !== 'object' || group === null) continue;
    const light = (group as Record<string, unknown>).light;
    if (light && typeof light === 'object') {
      const main = (light as Record<string, { value?: string }>).main;
      if (main?.value) {
        colors.push(main.value);
        names.push(groupName);
      }
    }
  }

  return { colors, names, numColors: colors.length };
}
