// ── Helpers ──

const kebab = (s: string) => s.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase();

const forEachAction = (
  data: PaletteData,
  cb: (name: string, mode: 'light' | 'dark', shade: string, value: string) => void
) => {
  (data.palette ?? []).forEach(entry => {
    const [name, palette] = Object.entries(entry)[0] ?? [];
    if (!name || !palette) return;
    (['light', 'dark'] as const).forEach(mode => {
      Object.entries(palette[mode]).forEach(([shade, value]) => {
        cb(name, mode, shade, value);
      });
    });
  });
};

// ── Native JSON ──

export function toJSON(data: PaletteData): string {
  return JSON.stringify(data, null, 2);
}

// ── DTCG ──

export function toDTCG(data: PaletteData): string {
  return JSON.stringify(paletteToDTCG(data), null, 2);
}

// ── Tokens Studio (Figma plugin format) ──

export function toTokensStudio(data: PaletteData): string {
  const global: Record<string, Record<string, Record<string, unknown>>> = { color: {} };

  forEachAction(data, (name, mode, shade, value) => {
    if (!global.color[name]) global.color[name] = {};
    if (!global.color[name][mode]) global.color[name][mode] = {};
    (global.color[name][mode] as Record<string, unknown>)[shade] = {
      value,
      type: 'color',
    };
  });

  // Grey
  if (data.themeTokens?.grey) {
    global.color.grey = {};
    (['light', 'dark'] as const).forEach(mode => {
      (global.color.grey as Record<string, unknown>)[mode] = Object.fromEntries(
        Object.entries(data.themeTokens!.grey[mode]).map(([k, v]) => [
          k,
          { value: v, type: 'color' },
        ])
      );
    });
  }

  // Utility
  if (data.themeTokens?.utility) {
    (['light', 'dark'] as const).forEach(mode => {
      Object.entries(data.themeTokens!.utility[mode]).forEach(([groupName, entries]) => {
        if (!global.color[groupName]) global.color[groupName] = {};
        (global.color[groupName] as Record<string, unknown>)[mode] = Object.fromEntries(
          Object.entries(entries).map(([k, v]) => [k, { value: v, type: 'color' }])
        );
      });
    });
  }

  return JSON.stringify({ global }, null, 2);
}

// ── CSS Variables ──

export function toCSS(data: PaletteData): string {
  const lightVars: string[] = [];
  const darkVars: string[] = [];

  forEachAction(data, (name, mode, shade, value) => {
    const varName = `--color-${kebab(name)}-${kebab(shade)}`;
    (mode === 'light' ? lightVars : darkVars).push(`  ${varName}: ${value};`);
  });

  if (data.themeTokens?.grey) {
    Object.entries(data.themeTokens.grey.light).forEach(([k, v]) =>
      lightVars.push(`  --color-grey-${k}: ${v};`)
    );
    Object.entries(data.themeTokens.grey.dark).forEach(([k, v]) =>
      darkVars.push(`  --color-grey-${k}: ${v};`)
    );
  }

  if (data.themeTokens?.utility) {
    (['light', 'dark'] as const).forEach(mode => {
      const target = mode === 'light' ? lightVars : darkVars;
      Object.entries(data.themeTokens!.utility[mode]).forEach(([group, entries]) => {
        Object.entries(entries).forEach(([key, value]) => {
          target.push(`  --color-${kebab(group)}-${kebab(key)}: ${value};`);
        });
      });
    });
  }

  return `:root {\n${lightVars.join('\n')}\n}\n\n[data-theme="dark"] {\n${darkVars.join('\n')}\n}\n`;
}

// ── SCSS Variables ──

export function toSCSS(data: PaletteData): string {
  const lines: string[] = ['// Generated by Palette Pally\n'];

  forEachAction(data, (name, mode, shade, value) => {
    lines.push(`$color-${kebab(name)}-${kebab(shade)}-${mode}: ${value};`);
  });

  if (data.themeTokens?.grey) {
    lines.push('\n// Grey');
    (['light', 'dark'] as const).forEach(mode => {
      Object.entries(data.themeTokens!.grey[mode]).forEach(([k, v]) =>
        lines.push(`$color-grey-${k}-${mode}: ${v};`)
      );
    });
  }

  if (data.themeTokens?.utility) {
    lines.push('\n// Utility');
    (['light', 'dark'] as const).forEach(mode => {
      Object.entries(data.themeTokens!.utility[mode]).forEach(([group, entries]) => {
        Object.entries(entries).forEach(([key, value]) => {
          lines.push(`$color-${kebab(group)}-${kebab(key)}-${mode}: ${value};`);
        });
      });
    });
  }

  return lines.join('\n') + '\n';
}

// ── MUI Theme (TypeScript) ──

const SEMANTIC_NAMES = ['primary', 'secondary', 'success', 'warning', 'info', 'error'];

export function toMuiTheme(data: PaletteData): string {
  const findVariant = (name: string, mode: 'light' | 'dark') => {
    const idx = data.names?.findIndex(n => n.toLowerCase() === name);
    if (idx === undefined || idx < 0) return null;
    const entry = data.palette?.[idx];
    if (!entry) return null;
    return Object.values(entry)[0]?.[mode] ?? null;
  };

  const buildPalette = (mode: 'light' | 'dark'): string => {
    const entries: string[] = [`    mode: '${mode}',`];

    SEMANTIC_NAMES.forEach(name => {
      const v = findVariant(name, mode);
      if (!v) return;
      entries.push(
        `    ${name}: {`,
        `      main: '${v.main}',`,
        `      dark: '${v.dark}',`,
        `      light: '${v.light}',`,
        `      contrastText: '${v.contrastText}',`,
        `    },`
      );
    });

    if (data.themeTokens?.grey?.[mode]) {
      entries.push('    grey: {');
      Object.entries(data.themeTokens.grey[mode]).forEach(([k, v]) =>
        entries.push(`      ${k}: '${v}',`)
      );
      entries.push('    },');
    }

    const u = data.themeTokens?.utility?.[mode];
    if (u?.text) {
      entries.push('    text: {');
      Object.entries(u.text).forEach(([k, v]) => entries.push(`      ${k}: '${v}',`));
      entries.push('    },');
    }
    if (u?.background) {
      entries.push('    background: {');
      Object.entries(u.background).forEach(([k, v]) => entries.push(`      ${k}: '${v}',`));
      entries.push('    },');
    }
    if (u?.divider?.default) {
      entries.push(`    divider: '${u.divider.default}',`);
    }

    return entries.join('\n');
  };

  return `import { createTheme } from '@mui/material/styles';

// Light theme
export const lightTheme = createTheme({
  palette: {
${buildPalette('light')}
  },
});

// Dark theme
export const darkTheme = createTheme({
  palette: {
${buildPalette('dark')}
  },
});
`;
}

// ── Tailwind Config ──

export function toTailwind(data: PaletteData): string {
  const colors: Record<string, Record<string, string> | string> = {};

  (data.palette ?? []).forEach(entry => {
    const [name, palette] = Object.entries(entry)[0] ?? [];
    if (!name || !palette) return;
    colors[name] = {
      DEFAULT: palette.light.main,
      light: palette.light.light,
      dark: palette.light.dark,
      lighter: palette.light.lighter,
      contrast: palette.light.contrastText,
    };
  });

  if (data.themeTokens?.grey) {
    const greyMap: Record<string, string> = {};
    Object.entries(data.themeTokens.grey.light).forEach(([k, v]) => {
      greyMap[k] = v;
    });
    colors.grey = greyMap;
  }

  return `/** @type {import('tailwindcss').Config} */
module.exports = {
  theme: {
    extend: {
      colors: ${JSON.stringify(colors, null, 8).replace(/^/gm, '      ').trim()},
    },
  },
};
`;
}

